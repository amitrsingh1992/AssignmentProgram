public class Loop
{

public static void main(String args[])
{

int [] numbers = {10,20,30,40};
for (int x : numbers)
{

System.out.print(x);
System.out.print("\t");

}

System.out.print("\n");

String [] names = {"Tom" , "Dick" , "Harry" , "Gabral"};

for( String name : names)
{

System.out.print(name);
System.out.print("\t");

}

System.out.println("\n");


}

}
